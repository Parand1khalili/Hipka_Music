package com.hipka.app.domain.model

/**
 * منشأ داده‌ای که به UI داده می‌شود. با این حالت می‌توان «آفلاین هستیم و داریم کش
 * را نشان می‌دهیم» را از «واقعاً چیزی برای نمایش نیست» تفکیک کرد.
 */
enum class DataSourceState {
    /** داده تازه از سرور گرفته شده */
    FRESH,

    /**
     * کش محلی به عنوان نمایش سریع اولیه نشان داده می‌شود در حالی که داده تازه در
     * حال گرفته شدن است. این حالت «آفلاین» نیست و نباید بنر آفلاین را نشان دهد.
     */
    CACHED_WHILE_LOADING,

    /** اینترنت نبود یا درخواست شکست خورد؛ داده از کش محلی می‌آید */
    CACHED,

    /** نه اینترنت هست و نه چیزی در کش موجود است */
    UNAVAILABLE
}

data class SongsResult(
    val songs: List<Song>,
    val sourceState: DataSourceState
)
